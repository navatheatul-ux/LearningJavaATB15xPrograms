package ex_06_Increament_Decreament_Op;

public class Lab0027_Increament_Op {

    public static void main(String[] args)
    {
        int a = 10;

        int result = ++a;

        System.out.println(a);
        System.out.println(result);

        // Expression & Result Table

        // Line No  |  a  |  Result
        //    7     |   10  |  NA
        //    9     |   11  |  11
        //    11    |   11  |  -
        //    12    |   -   |  11

    }
}
