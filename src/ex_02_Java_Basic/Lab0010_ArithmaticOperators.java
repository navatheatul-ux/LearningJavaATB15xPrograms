package ex_02_Java_Basic;

public class Lab0010_ArithmaticOperators {

    public static void main(String[] args)
    {
        int a = 250;
        short b = 50;

        float c = 1.5f;
        double d = 1.5;

        System.out.println("The addition is " +(a+b));   // These are nothing but the Arithmatic Operators which are used to perform specific operations on the operands
        System.out.println("The subtraction is " +(a-b));
        System.out.println("The multiplication is " +(a*c));
        System.out.println("The division is " +(b/c));
        System.out.println("The remainder is " +(a%d));
    }
}
