package ex_02_Java_Basic;

public class Lab0016_Char_Operations {

    public static void main(String[] args)
    {
        char a = 'A';

        char b = 'B';

        short s = 30;

        System.out.println(a);     // Here it always print the actual define char
        System.out.println(b);

        System.out.println(a+b);  // This will print the the interger value for the Char data type

        System.out.println(a == 65);

        System.out.println('A' == 65);

        System.out.println(b + s);
    }
}
